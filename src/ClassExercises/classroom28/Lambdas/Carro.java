package ClassExercises.classroom28.Lambdas;

public class Carro {
    private String matricula;
    private int ano;

    public Carro(String matricula, int ano) {
        this.matricula = matricula;
        this.ano = ano;
    }

    public String getMatricula() {
        return matricula;
    }

    public int getAno() {
        return ano;
    }

}
